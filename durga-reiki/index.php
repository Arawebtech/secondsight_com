<!-- Countdown Timer Script -->
<script>
// Set the target date (24 hours from now)
function setTargetDate() {
    const now = new Date();
    const target = new Date(now.getTime() + (24 * 60 * 60 * 1000)); // 24 hours from now
    return target;
}

// Update countdown timer
function updateCountdown(targetDate, countdownElement) {
    const now = new Date().getTime();
    const distance = targetDate - now;

    if (distance < 0) {
        // Reset timer when it reaches zero
        const newTarget = setTargetDate();
        updateCountdown(newTarget, countdownElement);
        return;
    }

    const days = Math.floor(distance / (1000 * 60 * 60 * 24));
    const hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
    const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
    const seconds = Math.floor((distance % (1000 * 60)) / 1000);

    // Update all countdown elements on the page
    const countdownElements = document.querySelectorAll('.countdown-timer');
    countdownElements.forEach(element => {
        const dayElements = element.querySelectorAll('.countdown-days');
        const hourElements = element.querySelectorAll('.countdown-hours');
        const minuteElements = element.querySelectorAll('.countdown-minutes');
        const secondElements = element.querySelectorAll('.countdown-seconds');

        dayElements.forEach(el => el.textContent = days.toString().padStart(2, '0'));
        hourElements.forEach(el => el.textContent = hours.toString().padStart(2, '0'));
        minuteElements.forEach(el => el.textContent = minutes.toString().padStart(2, '0'));
        secondElements.forEach(el => el.textContent = seconds.toString().padStart(2, '0'));
    });
}

// Initialize countdown when page loads
document.addEventListener('DOMContentLoaded', function() {
    const targetDate = setTargetDate();
    
    // Update countdown every second
    setInterval(() => {
        updateCountdown(targetDate);
    }, 1000);
    
    // Initial update
    updateCountdown(targetDate);
});
</script>

